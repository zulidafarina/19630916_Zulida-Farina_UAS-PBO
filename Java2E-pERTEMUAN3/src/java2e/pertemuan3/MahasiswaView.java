        textNama.setText(tabelMahasiswa.getValueAt(row, 2).toString());
        textAlamat.setText(tabelMahasiswa.getValueAt(row, 3).toString());
        String prodi = tabelMahasiswa.getValueAt(row, 4).toString();
        comboboxgoldar.setSelectedItem(tabelMahasiswa.getValueAt(row, 5).toString());
