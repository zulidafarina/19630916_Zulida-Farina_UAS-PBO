    private void buttonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahActionPerformed
        if(textNPM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NPM belum diinput");
            textNPM.requestFocus();
        }else if(textNama.getText().isEmpty()){
            JOptionPane.showConfirmDialog(null, "Nama belum diinput");
            textNama.requestFocus();
        }else if(textAlamat.getText().isEmpty()){
            JOptionPane.showConfirmDialog(null, "Alamat belum diinput");
            textAlamat.requestFocus();
        }else{
            try{
                String prodi = "TEKNIK INFORMATIKA";
                if(jRadioTI.isSelected()){
                    prodi = "SISTEM INFORMASI";

                }
                pst = con.prepareStatement("insert into mahasiswa values (?,?,?,?,?,?)");
                pst.setInt(1, 0);
                pst.setString(2, textNPM.getText());
                pst.setString(3, textNama.getText());
                pst.setString(4,textAlamat.getText());
                pst.setString(5, prodi);
                pst.setString(6,comboboxgoldar.getSelectedItem().toString());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data mahasiswa berhasil ditambahkan");
                ulang();
                tampil_data();
            }catch(Exception e){
                System.out.println(e.getMessage());
            }

        }
    }//GEN-LAST:event_buttonTambahActionPerformed

    private void buttonUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUbahActionPerformed
        if(textNPM.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "NPM belum diinput");
            textNPM.requestFocus();
        }else if(textNama.getText().isEmpty()){
            JOptionPane.showConfirmDialog(null, "Nama belum diinput");
            textNama.requestFocus();
        }else if(textAlamat.getText().isEmpty()){
            JOptionPane.showConfirmDialog(null, "Alamat belum diinput");
            textAlamat.requestFocus();
        }else{
            try{
                String prodi = "Teknik Informatika";
                if(jRadioTI.isSelected()){
                    prodi = "Sistem Informasi";

                }
                pst = con.prepareStatement("update mahasiswa set npm=?, nama_lengkap=?, alamat=?, prodi=?, goldar=? where id=?");
                pst.setInt(1, 0);
                pst.setString(1, textNPM.getText());
                pst.setString(2, textNama.getText());
                pst.setString(3,textAlamat.getText());
                pst.setString(4, prodi);
                pst.setString(5,comboboxgoldar.getSelectedItem().toString());
                pst.setInt(6, Integer.parseInt(labelID.getText()));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data mahasiswa berhasil diperbaharui");
                ulang();
                tampil_data();
            }catch(Exception e){
                System.out.println(e.getMessage());
            }

        }
    }//GEN-LAST:event_buttonUbahActionPerformed

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        int konfirmasi = JOptionPane.showConfirmDialog(null, "yakin ingin menghapus data mahasiswa ini?", "HAPUS DATA?", 0);
        if(konfirmasi==0){
            try{
                pst = con.prepareStatement("delete from mahasiswa where id =?");
                pst.setInt(1, Integer.parseInt(labelID.getText()));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Mahasiswa berhasil dihapus!");
                ulang();
                tampil_data();
            }catch(Exception e){
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

