private void get_id_pemilihan(){//menampilkan id pemilihan
        try {
            pst = con.prepareStatement("select * from pemilihan_tempat order by id_pemilihan desc limit 1");
            rs = pst.executeQuery();
            if(rs.next()){
                int id = rs.getInt(1) + 1;
                labelIDPemilihan.setText(id+"");
            }else{
                labelIDPemilihan.setText("1");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

private void tampil_tempat(){ //menampilkan data tempat pkl ke dalam combobox
        try {
            ComboTempat.addItem("Pilih Tempat PKL");
            pst = con.prepareStatement("select * from tempat_pkl");
            rs = pst.executeQuery();
            while(rs.next()){
                ComboTempat.addItem(rs.getString(2)); //memasukan nama tempat ke dalam combobox                       
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

