    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
    if(TanggalMulai.getDate()==null){
        JOptionPane.showMessageDialog(null, "Tangaal mulai belum dipilih!", "Gagal simpan",0);
        TanggalMulai.requestFocus();
    }else if(TanggalSelesai.getDate()==null){
        JOptionPane.showMessageDialog(null, "Tanggal selesai belum dipilih!", "Gagal simpan",0);
        TanggalSelesai.requestFocus();
    }else if(LabelIDTempat.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Tempat PKL belum dipilih!", "Gagal simpan",0);
        ComboTempat.requestFocus();
    }else if(TableMahasiswa.getRowCount()<=0){
        JOptionPane.showMessageDialog(null, "Data Mahasiswa belum dipilih!", "Gagal simpan",0);
        ComboTempat.requestFocus();
    }else{
        simpan_pemilihan();
        simpan_detail_pemilihan();
        JOptionPane.showMessageDialog(null, "Data Pemilihan tempat PKL berhasil disimpan!", "Sukses simpan data", 1);
    }
    }//GEN-LAST:event_ButtonSimpanActionPerformed

