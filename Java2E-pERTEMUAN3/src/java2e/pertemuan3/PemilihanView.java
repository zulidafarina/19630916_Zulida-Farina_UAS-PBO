 private void simpan_pemilihan(){
     try{
         SimpleDateFormat formatTanggal = new SimpleDateFormat("yyy-MM-dd");
         pst = con.prepareStatement("insert into pemilihan_tempat values(?,?,?,?)");
         pst.setInt(1, 0);
         pst.setString(2, formatTanggal.format(TanggalMulai.getDate()));
         pst.setString(3, formatTanggal.format(TanggalSelesai.getDate()));
         pst.setString(4, LabelIDTempat.getText());
         pst.executeUpdate();
     }catch(SQLException e){
         System.out.println(e.getMessage());
     }
 }
 
private void simpan_detail_pemilihan(){
    try{
        int i = 0;
        while(i<TableMahasiswa.getRowCount()){
            pst = con.prepareStatement("insert into pemilihan_detail values(?,?,?)");
            pst.setInt(1, 0);
            pst.setString(2, labelIDPemilihan.getText());
            pst.setString(3, TableMahasiswa.getValueAt(i, 0).toString());
            pst.executeUpdate();
            i++;
        }
    }catch (Exception e){
        System.out.println(e.getMessage());
    }
}
 
